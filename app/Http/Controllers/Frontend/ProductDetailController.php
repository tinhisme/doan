    public function getProductDetail(Request $request, $slug)
    {
        
        $arraySlugs = explode('-',$slug);
        $id = array_pop($arraySlugs);
        if($id){
            $product = Product::findOrFail($id);
            return view('frontend.pages.product_detail.index',compact('product'));
        }
        return redirect('/');
    }
